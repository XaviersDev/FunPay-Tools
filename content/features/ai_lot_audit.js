// ── Robust JSON extractor ─────────────────────────────────────────────────────
// Handles: ```json ... ```, extra text before/after, single quotes, trailing commas
function robustExtractJSON(text) {
    if (!text) return null;

    // 1. Strip markdown code fences: ```json ... ``` or ``` ... ```
    text = text.replace(/^```(?:json)?\s*/im, '').replace(/\s*```\s*$/im, '').trim();

    // 2. Strip leading/trailing non-JSON text
    // Find first [ or { and last ] or }
    const firstBracket = Math.min(
        text.indexOf('[') === -1 ? Infinity : text.indexOf('['),
        text.indexOf('{') === -1 ? Infinity : text.indexOf('{')
    );
    const lastArray  = text.lastIndexOf(']');
    const lastObject = text.lastIndexOf('}');

    if (firstBracket === Infinity) return null;

    const isArray  = text[firstBracket] === '[';
    const lastChar = isArray ? lastArray : lastObject;
    if (lastChar === -1 || lastChar < firstBracket) return null;

    let candidate = text.slice(firstBracket, lastChar + 1);

    // 3. Fix common AI JSON mistakes:
    // - Trailing commas before ] or }
    candidate = candidate.replace(/,(\s*[}\]])/g, '$1');
    // - Single quotes → double quotes (simple cases)
    candidate = candidate.replace(/'([^']+)'(\s*:)/g, '"$1"$2');
    candidate = candidate.replace(/:\s*'([^']*)'/g, ': "$1"');

    return candidate;
}

function robustParseArray(text) {
    const candidate = robustExtractJSON(text);
    if (!candidate || candidate[0] !== '[') return null;
    try {
        const parsed = JSON.parse(candidate);
        return Array.isArray(parsed) && parsed.length > 0 ? parsed : null;
    } catch (_) { return null; }
}

function robustParseObject(text) {
    const candidate = robustExtractJSON(text);
    if (!candidate || candidate[0] !== '{') return null;
    try {
        const parsed = JSON.parse(candidate);
        return typeof parsed === 'object' && parsed !== null ? parsed : null;
    } catch (_) { return null; }
}

