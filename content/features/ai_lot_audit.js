// ── Phase 1: Load user data ─────────────────────────────────────────────────
async function auditLoadData() {
    const raw = document.body?.dataset?.appData;
    if (!raw) throw new Error('Нет данных авторизации');
    const d = JSON.parse(raw);
    const u = Array.isArray(d) ? d[0] : d;
    const userId   = u?.userId;
    const username = u?.userName || 'Продавец';
    if (!userId) throw new Error('Нет userId');

    // Get lots via existing message handler
    const lots = await new Promise((resolve, reject) => {
        chrome.runtime.sendMessage({ action: 'getUserLotsList', userId }, (res) => {
            if (chrome.runtime.lastError) reject(new Error(chrome.runtime.lastError.message));
            else resolve(res || []);
        });
    });

    // Get reviews via fetch
    let reviews = [];
    try {
        const res = await fetch(`https://funpay.com/users/${userId}/?reviewsPage=1`, { credentials: 'include' });
        if (res.ok) {
            const html = await res.text();
            const doc  = new DOMParser().parseFromString(html, 'text/html');
            Array.from(doc.querySelectorAll('.review-item')).slice(0, 30).forEach(el => {
                const text   = el.querySelector('.review-item-text')?.textContent.trim() || '';
                const rClass = Array.from(el.querySelector('[class*="rating"]')?.classList || []).find(c => /^rating\d/.test(c));
                const stars  = rClass ? parseInt(rClass.replace('rating',''), 10) : 0;
                const lotT   = el.querySelector('.review-lot-title, .review-item-lot')?.textContent.trim() || '';
                if (text) reviews.push({ stars, text: text.slice(0, 120), lot: lotT });
            });
        }
    } catch (_) {}

    return { lots, reviews, username };
}

