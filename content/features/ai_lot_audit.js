// ── Main init ────────────────────────────────────────────────────────────────
function initializeAILotAudit() {
    const page = document.querySelector('.fp-tools-page-content[data-page="ai_audit"]');
    if (!page || page.dataset.initialized) return;
    page.dataset.initialized = 'true';

    const startBtn = document.getElementById('fp-audit-start-btn');
    const nextBtn  = document.getElementById('fp-audit-next-btn');
    const prevBtn  = document.getElementById('fp-audit-prev-btn');

    if (!startBtn) return;

    const AUDIT_COOLDOWN_KEY = 'fpAuditLastRun';
    const AUDIT_COOLDOWN_MS  = 30 * 24 * 60 * 60 * 1000; // 30 days

    async function auditCheckCooldown() {
        const d = await chrome.storage.local.get(AUDIT_COOLDOWN_KEY);
        const last = d[AUDIT_COOLDOWN_KEY] || 0;
        const elapsed = Date.now() - last;
        return { allowed: elapsed >= AUDIT_COOLDOWN_MS, remainMs: AUDIT_COOLDOWN_MS - elapsed };
    }

    async function auditApplyCooldownUI() {
        const { allowed, remainMs } = await auditCheckCooldown();
        const cooldownMsg = document.getElementById('fp-audit-cooldown-msg');
        if (!allowed) {
            startBtn.disabled = true;
            startBtn.style.opacity = '0.5';
            if (cooldownMsg) {
                const days  = Math.ceil(remainMs / (24 * 60 * 60 * 1000));
                const hours = Math.ceil(remainMs / (60 * 60 * 1000));
                cooldownMsg.style.display = 'block';
                cooldownMsg.textContent = days > 1
                    ? `⏳ Следующий аудит будет доступен через ${days} дн.`
                    : `⏳ Следующий аудит будет доступен через ${hours} ч.`;
            }
        } else {
            startBtn.disabled = false;
            startBtn.style.opacity = '';
            if (cooldownMsg) cooldownMsg.style.display = 'none';
        }
    }

    auditApplyCooldownUI();

    startBtn.addEventListener('click', async () => {
        const { allowed } = await auditCheckCooldown();
        if (!allowed) { showNotification('Аудит доступен раз в месяц.', true); return; }
        await chrome.storage.local.set({ [AUDIT_COOLDOWN_KEY]: Date.now() });
        auditApplyCooldownUI();
        auditSetState('loading');
        _answers = {};
        _questions = [];
        _currentQ = 0;

        try {
            document.getElementById('fp-audit-loading').textContent = '⏳ Загружаем лоты и отзывы...';
            _auditData = await auditLoadData();

            document.getElementById('fp-audit-loading').textContent = `✅ Загружено ${_auditData.lots.length} лотов, ${_auditData.reviews.length} отзывов.\n⏳ ИИ генерирует вопросы...`;
            _questions = await auditGenerateQuestions(_auditData);

            auditSetState('survey');
            auditRenderQuestion(0);

        } catch (e) {
            auditSetState('start');
            showNotification(`Ошибка: ${e.message}`, true);
        }
    });

    if (nextBtn) {
        nextBtn.addEventListener('click', async () => {
            const container = document.getElementById('fp-audit-q-container');
            const val = container?._getValue?.() ?? '';

            // Save answer
            _answers[_currentQ] = val;

            if (_currentQ >= _questions.length - 1) {
                // Finish — generate recommendations
                auditSetState('processing');
                try {
                    const recs = await auditGenerateRecommendations(_auditData, _questions, _answers);
                    auditShowResults(recs);
                } catch (e) {
                    auditSetState('survey');
                    showNotification(`Ошибка: ${e.message}`, true);
                }
            } else {
                auditRenderQuestion(_currentQ + 1);
                // Update button label
                nextBtn.textContent = _currentQ >= _questions.length - 1 ? '✅ Завершить' : 'Далее →';
            }

            // Update next button
            if (_currentQ < _questions.length - 1) {
                nextBtn.textContent = _currentQ + 1 >= _questions.length - 1 ? '✅ Завершить' : 'Далее →';
            }
        });
    }

    if (prevBtn) {
        prevBtn.addEventListener('click', () => {
            if (_currentQ > 0) {
                // Save current before going back
                const container = document.getElementById('fp-audit-q-container');
                const val = container?._getValue?.() ?? '';
                _answers[_currentQ] = val;
                auditRenderQuestion(_currentQ - 1);
                if (nextBtn) nextBtn.textContent = 'Далее →';
            }
        });
    }
}